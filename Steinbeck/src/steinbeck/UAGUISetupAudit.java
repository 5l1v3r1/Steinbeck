    /** performing add audit or edit audit job */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                
        // first check if everything is filled!!
        if(!checkFields()){
            UAGUIEntrance.gui.showErrorMessage("Fill all the inputs (except maybe password)");
            return;
        }
        
        // first test the connection 
        if(!UAGUIEntrance.gui.testConnection()){
            return;
        }
        
        this.setVisible(false);

        if(isAddWindow)
            UAGUIEntrance.gui.addAudit();
        else
            UAGUIEntrance.gui.editAudit();

        UAGUIEntrance.gui.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed
    
    private boolean checkFields(){
        if(         getIP().compareTo("") == 0
                ||  getPort().compareTo("") == 0
                ||  getUsername().compareTo("") == 0
                ||  getLocalPath().compareTo("") == 0
                ||  getRemotePath().compareTo("") == 0
                ||  getScriptName().compareTo("") == 0)
            return false;
        
        ip = getIP();
        username= getUsername();
        port = getPort();
        remotePath = getRemotePath();
        localPath = getLocalPath();
        return true;
    }
    
