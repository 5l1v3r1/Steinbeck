    private void fillOutFields(){
        setIP(ip);
        setPort(port);
        setUsername(username);
        setRemotePath(remotePath);
        setLocalPath(localPath);
        setPassword("");
        if((scriptName.compareTo("") != 0) && (fileSeparator.compareTo("") != 0))
            setAuditor(scriptName);
    }
    
